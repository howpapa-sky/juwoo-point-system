// 다음 유닛 가져오기
export function getNextUnit(currentUnitId: string): LearningUnit | undefined {
  const idx = LEARNING_PATH.findIndex((u) => u.id === currentUnitId);
  if (idx < 0 || idx >= LEARNING_PATH.length - 1) return undefined;
  return LEARNING_PATH[idx + 1];
}

// 전체 단어 수
export function getTotalWordCount(): number {
  return LEARNING_PATH.reduce((sum, unit) => sum + unit.targetWords.length, 0);
}
